    reg2 = r'^(([0-9a-f]){1,4}:){7}([0-9a-f]){1,4}$'
    if re.search(reg1, text):
        print('IPv4')
    elif re.search(reg2, text):
        print('IPv6')
    else:
        print('Neither')
 
